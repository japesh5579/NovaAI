"use client";

import React, { useState, useEffect, useMemo, useRef } from "react";
import { createPortal } from "react-dom";
import { motion, useTransform, useSpring, useMotionValue } from "framer-motion";
import { TiltCard } from "@/components/ui/tilt-card";
import { X } from "lucide-react";

export type AnimationPhase = "scatter" | "line" | "circle" | "bottom-strip";

interface FlipCardProps {
    src: string;
    index: number;
    total: number;
    phase: AnimationPhase;
    target: { x: number; y: number; rotation: number; scale: number; opacity: number };
    onClick: () => void;
}

const IMG_WIDTH = 60;
const IMG_HEIGHT = 85;

function FlipCard({ src, index, total, phase, target, onClick }: FlipCardProps) {
    return (
        <motion.div
            animate={{
                x: target.x,
                y: target.y,
                rotate: target.rotation,
                scale: target.scale,
                opacity: target.opacity,
            }}
            transition={{ type: "spring", stiffness: 40, damping: 15 }}
            style={{
                position: "absolute",
                width: IMG_WIDTH,
                height: IMG_HEIGHT,
                transformStyle: "preserve-3d",
                perspective: "1000px",
            }}
            className="cursor-pointer group"
            onClick={onClick}
        >
            <TiltCard tiltLimit={15} scale={1.12} effect="gravitate" spotlight={false}
                className="h-full w-full rounded-xl shadow-lg">
                <img src={src} alt={`hero-${index}`} className="h-full w-full object-cover rounded-xl" />
                <div className="absolute inset-0 bg-black/10 transition-colors group-hover:bg-black/0 rounded-xl" />
            </TiltCard>
        </motion.div>
    );
}

